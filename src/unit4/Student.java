package unit4;

public class Student {
	private int year;
	private String name;
	public Student(int year, String name) {
	this.year = year;
	this.name = name;
	}
	public int getYear() {
	return year;
	}
	public String getName() {
	return name;
	}
	public void setYear(int year)
	{
		this.year = year;
	}
	
}
